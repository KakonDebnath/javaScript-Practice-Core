let day = 5;

switch (day) {
      case 1:
            day = 'Today Is Saturday';
            break;
      case 2:
            day = 'Today Is Sunday';
            break;
      case 3:
            day = 'Today Is Monday';
            break;
      case 4:
            day = 'Today Is Tuesday';
            break;
      case 5:
            day = 'Today Is Wednesday';
            break;
      case 6:
            day = 'Today Is Thursday';
            break;
      case 7:
            day = 'Today Is Friday';
            break;

      default:
            day = 'not found';
            break;
}

console.log(day);